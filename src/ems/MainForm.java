    private void Employee_Table1AncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_Employee_Table1AncestorAdded
        // TODO add your handling code here:
        JTableHeader Theader = Employee_Table1.getTableHeader();
        Theader.setBackground(Color.black);
        Theader.setForeground(Color.GRAY);
         // Clear existing rows
    DefaultTableModel model = (DefaultTableModel) Employee_Table1.getModel();
    model.setRowCount(0); 

    try {
        // Establish database connection
        Connection conn = MainConnection.getConnection(); // Assuming MainConnection.getConnection() returns a valid Connection object

        // Check if the connection is valid
        if (conn == null) {
            JOptionPane.showMessageDialog(null, "Database connection is not initialized.");
            return; // Exit method if connection is not initialized
        }

        // Prepare SQL query
        String sql = "SELECT * FROM employee_list";
        PreparedStatement ps = conn.prepareStatement(sql);
        
        // Execute query
        ResultSet rs = ps.executeQuery();

        // Populate the table model with retrieved data
        while (rs.next()) {
            model.addRow(new Object[]{
                    rs.getString("srno"),
                    rs.getString("fname"),
                    rs.getString("lname"),
                    rs.getString("departement"),
                    rs.getString("designation"),
                    rs.getString("number"),
                    rs.getString("status"),
                    rs.getString("address"),
                    rs.getString("salary")
            });
        }

        // Close database resources
        rs.close();
        ps.close();
        conn.close();

    } catch (SQLException | ClassNotFoundException ex) {
        JOptionPane.showMessageDialog(null, "An error occurred while accessing the database: " + ex.getMessage());
    }
    }//GEN-LAST:event_Employee_Table1AncestorAdded

