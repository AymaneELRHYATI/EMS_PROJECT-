    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            conn = MainConnection.getConnection(); // Assuming MainConnection.getConnection() returns a valid Connection object
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Failed to establish database connection: " + ex.getMessage());
            return; // Exit method if connection fails
        }

        // Check if the connection is valid
        if (conn == null) {
            JOptionPane.showMessageDialog(null, "Database connection is not initialized.");
            return; // Exit method if connection is not initialized
        }

        // Execute INSERT statement
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to add this record ?", "Add record", JOptionPane.YES_NO_OPTION);
        if (p == 0) {
            try {
                String sql = "INSERT INTO `employee_list`(`fname`, `lname`, `departement`, `designation`, `number`, `status`, `address`, `salary`) VALUES (?,?,?,?,?,?,?,?)";
                ps = conn.prepareStatement(sql);

                // Set parameter values
                ps.setString(1, txtFname.getText());
                ps.setString(2, txtLname.getText());
                ps.setString(3, txtDepartement.getSelectedItem().toString());
                ps.setString(4, txtDesignation.getSelectedItem().toString());
                ps.setString(5, txtNumber.getText());
                ps.setString(6, status);
                ps.setString(7, txtAddress.getText());
                ps.setString(8, txtSalary.getText());

                // Execute the INSERT statement
                ps.execute();

                JOptionPane.showMessageDialog(null, "Data added successfully");
                reset();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error executing INSERT statement: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

