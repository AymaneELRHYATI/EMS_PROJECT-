    private void txtSearchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyPressed
        // TODO add your handling code here:
        try {
            conn = MainConnection.getConnection(); // Assuming MainConnection.getConnection() returns a valid Connection object
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Failed to establish database connection: " + ex.getMessage());
            return; // Exit method if connection fails
        }

        // Check if the connection is valid
        if (conn == null) {
            JOptionPane.showMessageDialog(null, "Database connection is not initialized.");
            return; // Exit method if connection is not initialized
        }
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        try {
            String sql = "SELECT `srno`, `fname`, `lname`, `departement`, `designation`, `number`, `status`, `address` FROM `employee_list` WHERE  srno = ?";
            ps = conn.prepareStatement(sql);
            ps.setString(1, txtSearch.getText());
            rs = ps.executeQuery();
            if (rs.next()) {
                txtFname1.setText(rs.getString(2));
                txtLname1.setText(rs.getString(3));
                txtDepartement1.setText(rs.getString(4));
                txtDesignation1.setText(rs.getString(5));
                txtNumber1.setText(rs.getString(6));
                status = rs.getString(7);
                if (status != null) {
                    if (status.equals("Active")) {
                        r_Active1.setSelected(true);
                        r_Inactive1.setSelected(false);
                    } else if (status.equals("In-Active")) {
                        r_Active1.setSelected(false);
                        r_Inactive1.setSelected(true);
                    } else {
                        // Handle unexpected status values here
                    }
                }
                txtAddress1.setText(rs.getString(8));
            } else {
                // Clear fields if no record found
                txtFname1.setText("");
                txtLname1.setText("");
                txtDepartement1.setText("");
                txtDesignation1.setText("");
                txtNumber1.setText("");
                r_Active1.setSelected(false);
                r_Inactive1.setSelected(false);
                txtAddress1.setText("");
                JOptionPane.showMessageDialog(null, "No matching record found.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "An error occurred while searching for the record.");
        }
    }
    }//GEN-LAST:event_txtSearchKeyPressed

